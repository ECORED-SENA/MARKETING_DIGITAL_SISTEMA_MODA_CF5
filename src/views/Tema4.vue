    p.mb-4 La lealtad hacia una marca supone una conexión con las emociones y lo que esta representa para el cliente en su cotidianidad. Por lo tanto, para las empresas, el objetivo máximo es conseguir ese nivel de lealtad de sus clientes, lo cual se traduce en importantes ventajas.  Según García y Gutiérrez (2013), es posible identificar dos componentes en la lealtad: el comportamental, que se puede reconocer con variables basadas en los comportamientos de compra medibles, como la frecuencia, el volumen, el tiempo de permanencia con una empresa, el tipo de productos adquiridos, entre otros; y el emocional, influenciado por el sentir del cliente y se reconoce en aspectos como el afecto, la actitud o el compromiso con una marca, de acuerdo con los valores que esta representa. 
    
    .row.mb-4
      .col-9.d-flex.align-items-center
        p Siguiendo la idea de García y Gutiérrez (2013), al unirse el componente comportamental y el emocional, se identifica una lealtad verdadera del cliente, lo que significa que esta es inherente a la personalidad del cliente, el cual siente la marca como si fuera parte de su ser y por ello sigue consumiendo sus productos. Por otro lado, la falsa lealtad se percibe en el momento en que un cliente compra regularmente una determinada marca, pero no lo hace más que por practicidad, aunque no se siente identificado con la marca ni tiene un vínculo más allá de una simple compra. La lealtad latente se define como ese sentimiento de querer adquirir un producto de determinada empresa, pero hay ciertos factores que imposibilitan o complican esta oportunidad. Puede ser por falta de poder adquisitivo o porque algunas características de la marca no acaban de convencer. Pero cuando no se ve necesidad alguna de ser fiel a una marca y se cambia de opciones en repetidas ocasiones es cuando no hay lealtad.
