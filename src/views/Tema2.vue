    p.mb-4 Con la llegada de Internet, fueron muchos los cambios evidenciados en el diario vivir. Uno de ellos, y puede que de los más importantes, fue la manera para comunicarnos a través de medios digitales. En estos nuevos entornos, se evidenció el uso de herramientas audiovisuales e interactivas, pero nunca se dejó de lado la comunicación escrita, identificada en los distintos portales web, páginas web, blogs y redes sociales. Sin embargo, es importante destacar que la comunicación escrita tuvo que adaptarse al nuevo entorno digital, donde, a diferencia de los medios tradicionales, reina la inmediatez y el afán de los usuarios. De esta forma es como surgen nuevas pautas para redactar en medios digitales, lo cual es imprescindible para toda persona que se desenvuelve en el mundo virtual.
