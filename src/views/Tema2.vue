        .h4 2.2  Bases para un plan de medios digitales
      
      p.mb-4 Según Soria (2016), el plan de medios se define como “el soporte que impulsa la difusión de la campaña de una marca para enviar un mensaje a los targets” (p.40); en otras palabras, es el proceso de comunicación que desarrolla una marca al emplear distintos medios para comunicar eficazmente sus contenidos, lo que a su vez debe suponer una optimización de recursos. Dejando a un lado los medios tradicionales, desde el entorno digital los distintos medios usados pueden ser: una página web, redes sociales, blogs con la combinación de elementos audiovisuales como videos, fotografías, transmisiones en vivo o podcasts; además de las diferentes plataformas que día a día se posicionan en diversos segmentos del mercado.
      p.mb-4 Las técnicas tradicionales que desarrolla una marca en la comunicación externa son adecuadas para utilizarse en el plan de medios adaptado al entorno digital (Soria, 2016),  lo cual, al lograr posicionamiento, se traduce en el incremento de ventas y la fidelización de clientes. En el siguiente recurso de aprendizaje, se pueden explorar algunas de ellas.

